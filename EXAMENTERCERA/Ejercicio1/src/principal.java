    private void leerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerButtonActionPerformed
        FileDialog fd = new FileDialog(this, "Elije un Fichero", FileDialog.LOAD);
        fd.setDirectory("C:\\temp"); //Opcional
        fd.setFile("*.txt"); //Opcional
        fd.setVisible(true);
        String filename = fd.getDirectory() + "/" + fd.getFile();
        if (fd.getFile() == null) {
            System.out.println("No has seleccionado ningÃºn fichero");
        } else {
            System.out.println("El fichero seleccionado es: " + filename);
        }

        jList1.setModel(lm);
        String nif = "";
        String nombre = "";
        String apellidos = "";
        String profesion = "";
        int edad = 0;
        int cont = 0;
        String linea;
        FileReader fr = null;
        BufferedReader br = null;

        try {
            fr = new FileReader(filename);
            br = new BufferedReader(fr);

            while ((linea = br.readLine()) != null) {

                cont++;
                if (cont == 0) {
                    nif = linea;
                } else if (cont == 1) {
                    nombre = linea;
                } else if (cont == 3) {
                    apellidos = linea;
                } else if (cont == 4) {
                    profesion = linea;
                } else if (cont == 5) {
                    edad = Integer.parseInt(linea);
                }

                if (cont == 5) {
                    Persona p = new Persona(nif, nombre, apellidos, profesion, edad);
                    ListaPersonas.add(p);

                    cont = 0;
                }
            }

            for (int i = 0; i < ListaPersonas.size(); i++) {
                lm.addElement(ListaPersonas.get(i));
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_leerButtonActionPerformed

