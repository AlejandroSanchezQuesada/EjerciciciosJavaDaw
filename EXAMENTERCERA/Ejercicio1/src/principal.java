    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed

        FileDialog fd = new FileDialog(this, "Elije un Fichero", FileDialog.SAVE);
        fd.setDirectory("C:\\temp"); //Opcional
        fd.setFile("*.txt"); //Opcional
        fd.setVisible(true);
        String filename = fd.getDirectory() + "/" + fd.getFile();
        if (fd.getFile() == null) {
            System.out.println("No has seleccionado ningÃºn fichero");
        } else {
            System.out.println("El fichero seleccionado es: " + filename);
        }
        
        String nif = "";
        String nombre = "";
        String apellidos = "";
        String profesion = "";
        int edad = 0;
        
        FileWriter fw = null;
        BufferedWriter bw = null;
        
        try {
            fw = new FileWriter(filename);
            bw = new BufferedWriter(fw);
                       
            for (int i = 0; i < ListaPersonas.size(); i++) {
            nif = ListaPersonas.get(i).getNif();
            bw.write(nif);
            bw.newLine();
            nombre = ListaPersonas.get(i).getNombre();
            bw.write(nombre);
            bw.newLine();
            apellidos = ListaPersonas.get(i).getApellidos();
            bw.write(apellidos);
            bw.newLine();
            profesion = ListaPersonas.get(i).getProfesion();
            bw.write(profesion);
            bw.newLine();
            edad = ListaPersonas.get(i).getEdad();
            bw.write(""+edad);
            bw.newLine();

            }

        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
        
            if (filename != null) {
                try {
                    bw.close();
                } catch (IOException ex) {
                    Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        
        }
        

    }//GEN-LAST:event_guardarButtonActionPerformed

