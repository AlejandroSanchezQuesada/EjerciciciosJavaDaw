    private void guardarTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarTxtActionPerformed
        FileWriter fw = null;
        BufferedWriter bw = null;
        String fichero = guardarDialogo();

        try {
            fw = new FileWriter(fichero);
            bw = new BufferedWriter(fw);

            for (int i = 0; i < ListaPersonas.size(); i++) {

                bw.write(ListaPersonas.get(i).getNif());
                bw.newLine();
                bw.write(ListaPersonas.get(i).getNombre());
                bw.newLine();
                bw.write(ListaPersonas.get(i).getApellidos());
                bw.newLine();
                bw.write(ListaPersonas.get(i).getProfesion());
                bw.newLine();
                bw.write("" + ListaPersonas.get(i).getEdad());
                bw.newLine();

            }

        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } finally {

            if (fichero != null) {
                try {
                    bw.close();
                } catch (IOException ex) {
                    Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }


    }//GEN-LAST:event_guardarTxtActionPerformed

    private void botonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirActionPerformed

        Persona nuevaPersona = new Persona();
        nuevaPersona.setNif(textoNIF.getText());
        nuevaPersona.setNombre(textoNombre.getText());
        nuevaPersona.setApellidos(textoApellidos.getText());
        nuevaPersona.setProfesion(textoProfesion.getText());
        if (!textoEdad.getText().equals("")) {
            nuevaPersona.setEdad(Integer.parseInt(textoEdad.getText()));
        }

        ListaPersonas.add(nuevaPersona);
        ListaGrafico.setModel(lm);

        lm.addElement(nuevaPersona.toString());


    }//GEN-LAST:event_botonAnadirActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed

        if (ListaGrafico.getSelectedIndex() == -1) {
            System.out.println("No has seleccionado ningun indice");
        } else {
            int indice = ListaGrafico.getSelectedIndex();
            if (JOptionPane.showConfirmDialog(rootPane, "¿Desea eliminar?", "Confirmación", JOptionPane.YES_NO_OPTION) == 0) {

                ListaPersonas.remove(indice);

                lm.remove(indice);

            }

        }


    }//GEN-LAST:event_botonEliminarActionPerformed

    private void leerTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerTxtActionPerformed
        try {
            lm.removeAllElements();
            ListaPersonas.removeAll(ListaPersonas);

            FileReader fr = null;
            BufferedReader br = null;
            String fichero = cargarDialogo();

            fr = new FileReader(fichero);
            br = new BufferedReader(fr);
            String linea;
            String nif = "";
            String nombre = "";
            String apellidos = "";
            String profesion = "";
            String edad = "";
            int edadN = 0;

            while ((linea = br.readLine()) != null) {

                nif = linea;
                nombre = br.readLine();
                apellidos = br.readLine();
                profesion = br.readLine();
                edad = br.readLine();
                edadN = Integer.parseInt(edad);

                Persona p = new Persona(nif, nombre, apellidos, profesion, edadN);
                ListaPersonas.add(p);

            }
            ListaGrafico.setModel(lm);
            for (int i = 0; i < ListaPersonas.size(); i++) {
                lm.addElement(ListaPersonas.get(i).toString());
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_leerTxtActionPerformed

    private void guardarSerieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarSerieActionPerformed
        String fichero = guardarDialogo();

        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(fichero));

            for (int i = 0; i < ListaPersonas.size(); i++) {
                Persona p = ListaPersonas.get(i);
                oos.writeObject(p);

            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_guardarSerieActionPerformed

    private void leerSerieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leerSerieActionPerformed
        lm.removeAllElements();
        ListaPersonas.removeAll(ListaPersonas);

        String fichero = cargarDialogo();
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream(fichero));
            ListaGrafico.setModel(lm);

            while (ois.readObject() != null) {
                lm.addElement(ois.readObject());

            }

        } catch (EOFException ax) {
            System.out.println("Fin fichero");
        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_leerSerieActionPerformed

