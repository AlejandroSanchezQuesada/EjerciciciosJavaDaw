    public String guardarDialogo() {

        FileDialog fd = new FileDialog(this, "Elije un Fichero", FileDialog.SAVE);
        fd.setDirectory("C:\\temp"); //Opcional
        fd.setFile("*.txt"); //Opcional
        fd.setVisible(true);
        String filename = fd.getDirectory() + "/" + fd.getFile();
        if (fd.getFile() == null) {
            System.out.println("No has seleccionado ningún fichero");
        } else {
            System.out.println("El fichero seleccionado es: " + filename);

        }

        return filename;

    }

