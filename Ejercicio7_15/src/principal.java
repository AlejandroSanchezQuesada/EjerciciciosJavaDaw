    private void bCreaFich3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCreaFich3ActionPerformed

        FileDialog ruta = new FileDialog(new Frame(), "Guardar", FileDialog.SAVE);
        ruta.setDirectory("C://");
        ruta.setFile(".bin");
        ruta.setVisible(true);
        String rutaF = ruta.getDirectory() + ruta.getFile();
        ruta3.setText(rutaF);

        File documento = null;
        FileReader fr = null;
        BufferedReader br = null;

        FileInputStream fis = null;
        DataInputStream dis = null;

        FileWriter documentoFinal = null;
        BufferedWriter pwFinal = null;

        try {
            documento = new File(ruta1.getText());
            fr = new FileReader(documento);
            br = new BufferedReader(fr);

            fis = new FileInputStream(ruta2.getText());
            dis = new DataInputStream(fis);

            documentoFinal = new FileWriter(ruta3.getText());
            pwFinal = new BufferedWriter(documentoFinal);

            for (int i = 0; i < 100; i++) {

                int n1 = br.read();
                int n2 = Integer.parseInt(dis.readUTF());
                int n3 = n1 + n2;
                System.out.print("  Numero 1 " + n1);
                System.out.print("  Numero 2 " + n2);
                System.out.print("  Numero 3 " + n3);
                System.out.println("");

                pwFinal.write(n1 + " + " + n2 + " = " + n3);
                pwFinal.newLine();
            }

        } catch (Exception e) {
        } finally {
            try {
                if (null != documentoFinal) {
                    pwFinal.close();
                }
            } catch (Exception e) {
            }
        }


    }//GEN-LAST:event_bCreaFich3ActionPerformed

