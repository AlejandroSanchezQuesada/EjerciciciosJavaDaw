    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String p = palabra.getText();
        String nueva = p.charAt(p.length() - 1) + p.substring(0, p.length() - 1);
        area.setText("");
        area.append(nueva+"\n");
        while (!p.equals(nueva)) {

            nueva = nueva.charAt(p.length() - 1) + nueva.substring(0, nueva.length() - 1);
            area.append(nueva+"\n");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

