    private void EmpleadosComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpleadosComboBoxActionPerformed

        
//Primero hacemos una sentencia para nombre y luego otra para Apellidos
        Statement sentenciaNombre;
        Statement sentenciaApellidos;
        try {
            sentenciaNombre = conexion.createStatement();
            ResultSet resultadoNombre = sentenciaNombre.executeQuery("SELECT Nombre FROM empleados WHERE IdEmpleado="+EmpleadosComboBox.getItemAt(EmpleadosComboBox.getSelectedIndex()));
            
            while(resultadoNombre.next()){
            String nombre = resultadoNombre.getString("Nombre");
            EmpleadoNombreTextField.setText(nombre);
            
            
            }
            //Ahora la de apellidos -----------------------------
            sentenciaApellidos = conexion.createStatement();
            ResultSet resultadoApellidos = sentenciaApellidos.executeQuery("SELECT Apellidos FROM empleados WHERE IdEmpleado="+EmpleadosComboBox.getItemAt(EmpleadosComboBox.getSelectedIndex()));
            
            while(resultadoApellidos.next()){
            String apellido = resultadoApellidos.getString("Apellidos");
            EmpleadoApellidoTextField.setText(apellido);
            
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        


    }//GEN-LAST:event_EmpleadosComboBoxActionPerformed

