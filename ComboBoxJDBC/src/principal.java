    private void PonmeIdEmpleadoEnComboBox(){
      try {
            conexion = DriverManager.getConnection(URL + DATABASE, USER, PASSWORD);

            Statement sentencia = conexion.createStatement();

            ResultSet resultado = sentencia.executeQuery("SELECT * FROM empleados");

            while (resultado.next()) {

                String IdEmpleado = String.valueOf(resultado.getInt(resultado.findColumn("IdEmpleado")));
                EmpleadosComboBox.addItem(IdEmpleado);

            }

        } catch (SQLException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }
    
    
    
    
