    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String apellidos = jTextField2.getText();
        String edad = jTextField3.getText();
        int edadN = Integer.parseInt(edad);

        Alumno p = new Alumno(nombre,apellidos,edadN);
        
        Lista.add(p);
        
        jList1.setModel(lm);
        lm.addElement(p);
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("fichero.txt"));
            
            oos.writeObject(Lista);
            
            
            
        } catch (IOException ex) {
            Logger.getLogger(FAlumnos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream("fichero.txt"));
            
            Lista = (ArrayList) ois.readObject();
            jList1.setModel(lm);
            
            for (Alumno p : Lista) {
                lm.addElement(p.toString());
            }
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FAlumnos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FAlumnos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FAlumnos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

